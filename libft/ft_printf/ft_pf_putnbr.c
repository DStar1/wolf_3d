#include "ft_printf.h"

void	ft_pf_putnbr(uintmax_t n)
{
	if (n >= 10)
	{
		ft_pf_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
